import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LinearRegression
from sklearn.linear_model import Lasso
from sklearn import metrics

car_dataset = pd.read_csv('car data.csv')

car_dataset.head()

car_dataset.shape

car_dataset.info()

car_dataset.isnull().sum()

print(car_dataset.Fuel_Type.value_counts())
print(car_dataset.Seller_Type.value_counts())
print(car_dataset.Transmission.value_counts())

# encoding "Fuel_Type" Column
car_dataset.replace({'Fuel_Type':{'Petrol':0,'Diesel':1,'CNG':2}},inplace=True)

# encoding "Seller_Type" Column
car_dataset.replace({'Seller_Type':{'Dealer':0,'Individual':1}},inplace=True)

# encoding "Transmission" Column
car_dataset.replace({'Transmission':{'Manual':0,'Automatic':1}},inplace=True)

car_dataset.head()

X = car_dataset.drop(['Car_Name','Selling_Price'],axis=1)
Y = car_dataset['Selling_Price']

X_train, X_test, Y_train, Y_test = train_test_split(X, Y, test_size = 0.1, random_state=2)

lin_reg_model = LinearRegression()

lin_reg_model.fit(X_train,Y_train)

training_data_prediction = lin_reg_model.predict(X_train)

error_score = metrics.r2_score(Y_train, training_data_prediction)
print("R squared Error : ", error_score)


test_data_prediction = lin_reg_model.predict(X_test)

error_score = metrics.r2_score(Y_test, test_data_prediction)
print("R squared Error : ", error_score)

import streamlit as st
import pickle
import numpy as np

st.title("Car Price Prediction App")

# Load model
with open("model.pkl", "rb") as f:
    model = pickle.load(f)

st.header("Enter Car Details")

# Inputs required by trained model
year = st.number_input("Year", min_value=1990, max_value=2025, step=1)
present_price = st.number_input("Present Price (Lakhs)")
kms_driven = st.number_input("Kms Driven")
fuel_type = st.selectbox("Fuel Type", ["Petrol", "Diesel", "CNG"])
seller_type = st.selectbox("Seller Type", ["Dealer", "Individual"])
transmission = st.selectbox("Transmission", ["Manual", "Automatic"])
owner = st.number_input("Previous Owners", min_value=0, max_value=3)

# Encoding (same as training)
fuel_map = {"Petrol": 0, "Diesel": 1, "CNG": 2}
seller_map = {"Dealer": 0, "Individual": 1}
trans_map = {"Manual": 0, "Automatic": 1}

if st.button("Predict"):
    input_data = np.array([[ 
        year,
        present_price,
        kms_driven,
        fuel_map[fuel_type],
        seller_map[seller_type],
        trans_map[transmission],
        owner
    ]])

    prediction = model.predict(input_data)
    st.success(f"Predicted Selling Price: â‚¹ {prediction[0]:.2f} Lakhs")
